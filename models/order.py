class Order():

    def __init__(self, customer_name, order_date, quantity, title, author, description, 
                 price, type):
        self.customer_name = customer_name
        self.order_date = order_date
        self.quantity = quantity
        self.title = title
        self.author = author
        self.description = description
        self.price = price
        self.type = type






